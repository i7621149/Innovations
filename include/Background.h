#ifndef BACKGROUND_H__
#define BACKGROUND_H__

#include "Entity.h"
//----------------------------------------------------------------------------------------------------------------------
/// @file Background.h
/// @brief inherits from Entity and is a simple quad object
/// @author Felix Marrington-Reeve
/// @version 0.1
/// @class Background
/// @brief basically draws a quad fullscreen using whatever shader is set
//----------------------------------------------------------------------------------------------------------------------
class Background : public Entity
{
public :
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief sets matID to 0 for background
  //----------------------------------------------------------------------------------------------------------------------
  Background();
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief default destructor
  //----------------------------------------------------------------------------------------------------------------------
  ~Background() = default;

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief update does nothing as quad is static
  //----------------------------------------------------------------------------------------------------------------------
  void update() {;}
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief draw the quad
  //----------------------------------------------------------------------------------------------------------------------
  void draw();

  //----------------------------------------------------------------------------------------------------------------------
  /// @brief initialise quad VAO
  //----------------------------------------------------------------------------------------------------------------------
  void createQuad();

private :
  //----------------------------------------------------------------------------------------------------------------------
  /// @brief identity of VAO
  //----------------------------------------------------------------------------------------------------------------------
  GLuint m_vaoID;
};

#endif//BACKGROUND_H__
